public class kmToMiles {
    public static void main(String[] args) {
        double distance = 10.8; // Create a variable distance, assign value 10.8
        double miles = 0.621371 * distance; // calculate miles and store in a variable miles by multiplying distance with 0.621371
        System.out.println("The distance "+distance+" km in miles is "+miles); // Print the distance in miles
    }
}
